package DataManage.JsonFormat;

public class MemberInfo {

	public String id;
	public String pw;
	public String name;
	public String number;
	public int age;
	
	public MemberInfo() {}
	
	public MemberInfo(String id, String pw, String name, String number, int age) {
		this.id = id;
		this.pw = pw;
		this.name = name;
		this.number = number;
		this.age = age;
	}

}
